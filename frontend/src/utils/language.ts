const storageKey = 'xingyunpan.locale';

export function normalizeLanguage(value?: string | null): AppLanguageCode {
  if (value === 'en-US' || value === 'English' || value === 'en') return 'en-US';
  return 'zh-CN';
}

export function normalizeTemplateLanguageCode(value?: string | null): string {
  const trimmed = String(value || '').trim();
  const aliases: Record<string, string> = {
    English: 'en-US',
    en: 'en-US',
    'zh-Hans': 'zh-CN',
    zh: 'zh-CN',
    Deutsch: 'de-DE',
    de: 'de-DE',
    'Espa\u00f1ol': 'es-ES',
    es: 'es-ES',
    'Fran\u00e7ais': 'fr-FR',
    fr: 'fr-FR',
    Italiano: 'it-IT',
    it: 'it-IT',
    '\u65e5\u672c\u8a9e': 'ja-JP',
    ja: 'ja-JP',
  };
  return aliases[trimmed] || trimmed || 'en-US';
}

export function templateLanguageLabel(code?: string | null): string {
  const normalized = normalizeTemplateLanguageCode(code);
  return emailTemplateLanguagePresets.find((item) => item.code === normalized)?.label || normalized;
}

export const currentLanguage = ref<AppLanguageCode>(
  normalizeLanguage(window.localStorage.getItem(storageKey) || document.documentElement.lang),
);

export function setAppLanguage(value?: string | null): AppLanguageCode {
  const language = normalizeLanguage(value);
  currentLanguage.value = language;
  document.documentElement.lang = language;
  window.localStorage.setItem(storageKey, language);
  return language;
}

